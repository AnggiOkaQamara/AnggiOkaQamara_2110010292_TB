    private void SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanActionPerformed
        // TODO add your handling code here:
        Perlengkapan perlengkapan = new Perlengkapan();
        perlengkapan.setNim(fnim.getText());
        perlengkapan.setNamaayah(fayah.getText());
        perlengkapan.setPekerjaanayah(fpayah.getText());
        perlengkapan.setNamaibu(fibu.getText());
        perlengkapan.setPekerjaanibu(fpibu.getText());
        perlengkapan.setAlamat(falamat.getText());

        if( fnim.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Lengkapi data");
        }else{

            Koneksi koneksi = new Koneksi();
            Connection con = koneksi.getConnection();
            PreparedStatement ps;
            try {
                    String qry = "insert into pelengkap_data (nim,nama_ayah,pekerjaan_ayah,nama_ibu,pekerjaan_ibu,alamat) values (?,?,?,?,?,?)";

                        ps = con.prepareStatement(qry);
                        ps.setString(1, perlengkapan.getNim());
                        ps.setString(2, perlengkapan.getNamaayah());
                        ps.setString(3, perlengkapan.getPekerjaanayah());
                        ps.setString(4, perlengkapan.getNamaibu());
                        ps.setString(5, perlengkapan.getPekerjaanibu());
                        ps.setString(6,perlengkapan.getAlamat());
                        ps.executeUpdate();
                        RefreshData();
                        resetForm();
             }catch(SQLException e){
                System.err.println("error :"+e);
            }
        }

    }//GEN-LAST:event_SimpanActionPerformed

    private void HapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HapusActionPerformed
        // TODO add your handling code here:
         int i = table2.getSelectedRow();
        if(i>=0){
        int pilihan = JOptionPane.showConfirmDialog(
                null,
                "ingin dihapus ?",
                "Konfirmasi hapus",
                JOptionPane.YES_NO_OPTION);
        if(pilihan==0){
                try{
                    TableModel model = table2.getModel();
                    Koneksi koneksi = new Koneksi();
                    Connection con = koneksi.getConnection();
                    String executeQuery = "delete from pelengkap_data where nim=?";
                    PreparedStatement ps = con.prepareStatement(executeQuery);
                    ps.setString(1, model.getValueAt(i, 0).toString());
                    ps.executeUpdate();
                        RefreshData();
                        resetForm();
                }  catch (SQLException ex){
                    System.err.println(ex);
                }
            }
            }else{
                JOptionPane.showMessageDialog(null, "Pilih data yang ingin dihapus");
        }
    }//GEN-LAST:event_HapusActionPerformed

