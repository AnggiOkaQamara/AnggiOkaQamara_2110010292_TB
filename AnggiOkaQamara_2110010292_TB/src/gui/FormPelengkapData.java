    public ArrayList<Perlengkapan> getPerlengkapanList () {
        ArrayList<Perlengkapan> PerlengkapanList = new ArrayList<Perlengkapan>();
        Koneksi koneksi = new Koneksi();
        Connection connection = koneksi.getConnection();

        PreparedStatement ps;
        ResultSet rs;
        String query = "SELECT * From pelengkap_data";
        String order = " ORDER BY pelengkap_data.nim";
//        if(!keyword.equals(""))
//            query = query+ " WHERE anggota.id = ? OR nim like ?";

        query = query+order;
        try{
            ps = connection.prepareStatement(query);
//            if(!keyword.equals("")){
//                ps.setString(1,eCari.getText());
//                ps.setString(2, "%"+eCari.getText()+"%");
//            }
            rs = ps.executeQuery();
            while(rs.next()){
               perlengkapan = new Perlengkapan(
                        rs.getString("nim"),
                        rs.getString("nama_ayah"),
                        rs.getString("pekerjaan_ayah"),
                        rs.getString("nama_ibu"),
                       rs.getString("pekerjaan_ibu"),
                        rs.getString("alamat"));
                PerlengkapanList.add(perlengkapan);
            }
        } catch(SQLException ex){
            System.err.println("ERROR PerlengkapanList : "+ex);
            System.err.println(ex.getMessage());
        }
        return PerlengkapanList;
    } 
     public void selectPerlengkapan(){
        ArrayList<Perlengkapan> list;
        list = getPerlengkapanList();
        DefaultTableModel model = (DefaultTableModel)table2.getModel();
        Object[] row =  new Object[6];

        for (int i=0; i<list.size(); i++){
            row[0] = list.get(i).getNim();
            row[1] = list.get(i).getNamaayah();
            row[2] = list.get(i).getPekerjaanayah();
            row[3] = list.get(i).getNamaibu();
            row[4] = list.get(i).getPekerjaanibu();
            row[5] = list.get(i).getAlamat();
            model.addRow(row);
        }
    }
  public final void RefreshData(){
        DefaultTableModel  dtmbk = (DefaultTableModel) table2.getModel();
        dtmbk.setRowCount(0);
        selectPerlengkapan();
    }

